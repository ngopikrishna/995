import cv2
import numpy as np

def display_image(list_of_lists):
    # Convert the list of lists to a NumPy array
    array = np.array(list_of_lists, dtype=np.uint8)

    # Create a grayscale image from the array
    image = cv2.cvtColor(array, cv2.COLOR_GRAY2BGR)

    # Display the image
    cv2.imshow("Grayscale Image", image)
    cv2.waitKey(0)
    cv2.destroyAllWindows()

